import streamlit as st
import pandas as pd
import plotly.express as px

# GitHub raw URL
file_url = "https://raw.githubusercontent.com/skywind99/25vibecoding1/main/pages/202504_202504_%E1%84%8B%E1%85%A7%E1%86%AB%E1%84%85%E1%85%A7%E1%86%BC%E1%84%87%E1%85%A7%E1%86%AF%E1%84%8B%E1%85%B5%E1%86%AB%E1%84%80%E1%85%AE%E1%84%92%E1%85%A7%E1%86%AB%E1%84%92%E1%85%AA%E1%86%BC_%E1%84%8B%E1%85%AF%E1%86%AF%E1%84%80%E1%85%A1%E1%86%AB.csv"

# CSV 파일 로드
data_1 = pd.read_csv(file_url, encoding='ISO-8859-1')

# 실제 컬럼 수 확인
st.write("파일의 컬럼명:", data_1.columns)
st.write("컬럼 수:", len(data_1.columns))

# 데이터 확인
st.write("파일의 일부 데이터:", data_1.head())

# 컬럼 수에 맞게 컬럼명 수정
# 예를 들어, 실제 컬럼 수가 27일 경우
expected_columns = ['지역', '2025년04월_연령별_인구', '2025년04월_연령별_합계', '2025년04월_0~9', '2025년04월_10~19', 
                    '2025년04월_20~29', '2025년04월_30~39', '2025년04월_40~49', '2025년04월_50~59', 
                    '2025년04월_60~69', '2025년04월_70~79', '2025년04월_80~89', '2025년04월_90~99', 
                    '2025년04월_100이상', '2025년04월_여성_연령별_인구', '2025년04월_여성_연령별_합계', '2025년04월_여성_0~9', 
                    '2025년04월_여성_10~19', '2025년04월_여성_20~29', '2025년04월_여성_30~39', '2025년04월_여성_40~49', 
                    '2025년04월_여성_50~59', '2025년04월_여성_60~69', '2025년04월_여성_70~79', '2025년04월_여성_80~89', 
                    '2025년04월_여성_90~99', '2025년04월_여성_100이상']

# 실제 컬럼의 수가 기대한 수와 다르면, 컬럼명 수정이 필요
if len(data_1.columns) == len(expected_columns):
    data_1.columns = expected_columns
else:
    st.error("컬럼 수가 예상한 값과 일치하지 않습니다.")

# 필요한 컬럼만 추출하여 시각화
data_1_filtered = data_1[['지역', '2025년04월_0~9', '2025년04월_10~19', '2025년04월_20~29', 
                           '2025년04월_30~39', '2025년04월_40~49', '2025년04월_50~59', 
                           '2025년04월_60~69', '2025년04월_70~79', '2025년04월_80~89']]

# 숫자 데이터로 변환
data_1_filtered = data_1_filtered.apply(pd.to_numeric, errors='coerce')

# Plotly로 연령대별 인구 분포 시각화
fig = px.bar(data_1_filtered, 
             x='지역', 
             y=['2025년04월_0~9', '2025년04월_10~19', '2025년04월_20~29', 
                 '2025년04월_30~39', '2025년04월_40~49', '2025년04월_50~59', 
                 '2025년04월_60~69', '2025년04월_70~79', '2025년04월_80~89'], 
             title="연령대별 인구 분포",
             labels={'value': '인구수', 'variable': '연령대'},
             barmode='stack')

# 그래프 출력
st.plotly_chart(fig)
